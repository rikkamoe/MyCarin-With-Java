    public void run() {  
        new Thread() {  
            public void run() {  
                int waktu = 0;  
                while (waktu == 0) {  
                    Calendar kalender = Calendar.getInstance();  

                    int tanggal = kalender.get(Calendar.DATE);
                    int bulan = kalender.get(Calendar.MONTH);
                    String namabulan = "";
                        if (bulan == 0){
                            namabulan = "Jan";
                        } else if (bulan == 1) {
                            namabulan = "Feb";
                        } else if (bulan == 2) {
                            namabulan = "Mar";
                        } else if (bulan == 3) {
                            namabulan = "Apr";
                        } else if (bulan == 4) {
                            namabulan = "May";
                        } else if (bulan == 5) {
                            namabulan = "Jun";
                        } else if (bulan == 6) {
                            namabulan = "Jul";
                        }   else if (bulan == 7) {
                            namabulan = "Aug";
                        } else if (bulan == 8) {
                            namabulan = "Sep";
                        } else if (bulan == 9) {
                            namabulan = "Oct";
                        } else if (bulan == 10) {
                            namabulan = "Nov";
                        } else if (bulan == 11) {
                            namabulan = "Des";
                        }
                    int tahun = kalender.get(Calendar.YEAR);
                    int jam = kalender.get(Calendar.HOUR);  
                    int menit = kalender.get(Calendar.MINUTE);  
                    int detik = kalender.get(Calendar.SECOND);  
                    int am_pm = kalender.get(Calendar.AM_PM);

                    String siang_malam = "";  
                    if (am_pm == 1) {  
                        siang_malam = "PM";  
                    } else {  
                        siang_malam = "AM";  
                    }  
 
                    String format_waktu = tanggal + "/" + namabulan + "/" + tahun + " "+ jam + ":" + menit + ":" + detik + " " + siang_malam;  

                    Time1.setText(format_waktu);  
                }  
            }  
        }.start();//ini wajib  
    }  
    
