    private void BtnCountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCountActionPerformed
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();
        formatRp.setCurrencySymbol("Rp. ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');
        kursIndonesia.setDecimalFormatSymbols(formatRp);
        
        if (InputNama.getText().isEmpty() || Length.getText().isEmpty() || Harga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Maaf , Form yang Diisi Kurang Lengkap !!");
        }
        else {
            String harga = Harga.getText();
            Number kursharga = null;
                try {
                    kursharga = kursIndonesia.parse(harga);
                } catch (ParseException ex) {
                    Logger.getLogger(rent.class.getName()).log(Level.SEVERE, null, ex);
                }
            double doubleharga = kursharga.doubleValue();
            String defharga; 
            defharga = String.format("%.2f",doubleharga);
            String lama_sewa = Length.getText();
        
            try {
                int a = Integer.parseInt(lama_sewa); 
                double b = Double.parseDouble(defharga);

                double hasil = b * a;

                Total.setText(kursIndonesia.format(hasil));
                
                int defhasil = (int) hasil;
                String total = String.valueOf(defhasil);
                TotalPayment.setText("Rp. "+total);
            }catch(NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Maaf , Form yang Diisi Tidak Sesuai !!");
            } 
        }
    }//GEN-LAST:event_BtnCountActionPerformed

    private void BtnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPayActionPerformed
        DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols formatRp = new DecimalFormatSymbols();
        formatRp.setCurrencySymbol("Rp. ");
        formatRp.setMonetaryDecimalSeparator(',');
        formatRp.setGroupingSeparator('.');
        kursIndonesia.setDecimalFormatSymbols(formatRp);
        
        if (Amount.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Maaf , Form yang Diisi Kurang Lengkap !!");
        } else {
            String totalbayar = TotalPayment.getText();
            Number kurstotal = null;
                try {
                    kurstotal = kursIndonesia.parse(totalbayar);
                } catch (ParseException ex) {
                    Logger.getLogger(rent.class.getName()).log(Level.SEVERE, null, ex);
                }
            double doubletotal = kurstotal.doubleValue();
            String deftotal; 
            deftotal = String.format("%.2f",doubletotal);
            String bayar = Amount.getText();
            
            try {
                double a = Double.parseDouble(deftotal);
                int b = Integer.parseInt(bayar);

                double kembalian = b - a;
                
                if(kembalian < 0){
                    JOptionPane.showMessageDialog(null, "Maaf , Uang Anda Kurang !!");
                }else{
                    Kembalian.setText(kursIndonesia.format(kembalian));
                    
                    String nama = InputNama.getText();
                    String tanggal = Time1.getText();
                    String number = String.valueOf(ComboNumber.getSelectedItem());
                    String type = Trans.getText();
                    String price = Harga.getText();
                    String length = Length.getText();
                    String total = Total.getText();
                    String bayar1 = Amount.getText();
                    double defbayar = Double.parseDouble(bayar1);
                    String kembalian1 = Kembalian.getText();
                    
                    NoteTitleName.setText("Name Driver");
                    NoteValueName.setText(nama);
                    NoteTitleDate.setText("Date Transaction");
                    NoteValueDate.setText(tanggal);
                    NoteTitleNumber.setText("Number Police");
                    NoteValueNumber.setText(number);
                    NoteTitleType.setText("Type");
                    NoteValueType.setText(type);
                    NoteTitlePrice.setText("Price");
                    NoteValuePrice.setText(price);
                    NoteTitleLength.setText("Length of Rental");
                    NoteValueLength.setText(length);
                    NoteTitleDays.setText("Days");
                    NoteTitleTotal.setText("Total Rent");
                    NoteValueTotal.setText(total);
                    NoteTitleAmount.setText("Amount");
                    NoteValueAmount.setText(kursIndonesia.format(defbayar));
                    NoteTitleChange.setText("Change");
                    NoteValueChange.setText(kembalian1);
                    
                }
            }catch(NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Maaf , Form yang Diisi Tidak Sesuai !!");
            }
        }
    }//GEN-LAST:event_BtnPayActionPerformed
